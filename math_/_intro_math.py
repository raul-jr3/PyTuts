
# BASIC MATH OPERATIONS

a = 10
b = 20

# addition, sub, multiplication, division of two numbers is as simple as writing 'a + b'
c = a + b
d = a - b
e = a * b

# IN python 2.7: division between two integers gives out an integer value
# so dividing 10 / 20 gives 0 instead of giving 0.5
# one way to work around this problem is to use the float constructor
f = float(a) / float(b)

print(c)
print(d)
print(e)
print(f)