

# common string methods
a = "raHuL"
print(a)

# UPPER
# to make all the letters uppercase, use the upper() on the string
print(a.upper())

# CAPITALIZE
# to make only the first letter of a string, use capitalize() on the string
print(a.capitalize())


# LOWER
print(a.lower())

# SWAPCASE
print(a.swapcase())

# to get the ascii value of a letter, use the ord
print(ord("r"))

